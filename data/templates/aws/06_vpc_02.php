<div><b>2. How do you secure your EC2 Instances running in a VPC (Virtual Private Cloud)?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
<div>Security Groups can be used to protect our EC2 Instances in a VPC. We can 
configure both INBOUND and OUTBOUND Traffic in a Security Group which enables 
secured access to our EC2 Instances. Security Group automatically denies any
unauthorized access to our EC2 Instances.
</div>
<!-- Answer ::: END -->
</div>
</div>


<div><b>4. Can you establish a peering connection to a Virtual Private Cloud (VPC) in a different Region?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
<div>It's not possible. Peering Connections are available only between VPC 
in the same Region.
</div>
<!-- Answer ::: END -->
</div>
</div>



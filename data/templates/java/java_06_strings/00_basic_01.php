<div><b>3. What are constants and how to create constants in java?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
Constants are fixed values whose values cannot be changed during the execution of program. We create
constants in java using final keyword.
Example : 
<pre><code class="language-html hljs xml">final int number = 10;
final String str = "java-interview–questions"</code></pre>


<div><b>1. What is difference between length and length() method in java?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
<b>length() :</b> In String class we have <code><b>length()</b></code> method which is used to return the number of characters in string.<br/>
<i>For Example,</i> 
<pre><code class="language-html hljs xml">String str = "Hello World";
System.out.println(str.length());</code></pre>
<code><b>str.length()</b></code> will return <code><b>11</b></code> characters including space.
<b>length :</b> we have length instance variable in arrays which will return the number of values or objects in array.
<i>For example,</i>
<pre><code class="language-html hljs xml">String days[]={” Sun”,”Mon”,”wed”,”thu”,”fri”,”sat”};</code></pre>
Will return <code><b>6</b></code> since the number of values in days array is <code><b>6</b></code>.

<div><b>2. Difference between Character Constant and String Constant in java?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
<code><b>Character constant</b></code> is enclosed in single quotes. String constants are enclosed in double quotes. Character constants are single digit or character.<br/><br/>
<i>Example :</i> ’2’, ‘A’<br/><br/>
<code><b>String Constants</b></code> are collection of characters.<br/><br/>
<i>Example :</i> "Hello World"
<!-- Answer ::: END -->
</div>

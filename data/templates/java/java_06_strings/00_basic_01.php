<div><b>4. What does null mean in java?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
When a reference variable doesn’t point to any value it is assigned <code><b>null</b></code>.<br/><br/>
<i>Example :</i>
<pre><code class="language-html hljs xml">Employee employee;</code></pre>
In the above example, <code><b>employee</b></code> object is not instantiate so it is pointed no where.

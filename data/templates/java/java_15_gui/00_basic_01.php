
<div><b>1. What is the difference between a Choice and a List?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
A Choice is displayed in a compact form that must be pulled down, in order for a user to be able to see the list of all available 
choices. Only one item may be selected from a Choice. A List may be displayed in such a way that several List items are visible. 
A List supports the selection of one or more List items.
<!-- Answer ::: END -->
</div>
</div>

<div><b>2. What is a layout manager?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
A layout manager is the used to organize the components in a container.
<!-- Answer ::: END -->
</div>
</div>

<div><b>3. What is the difference between a Scrollbar and a JScrollPane?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
A <code><b>Scrollbar</b></code> is a Component, but not a <code><b>Container</b></code>.<br/><br/>
A <code><b>ScrollPane</b></code> is a Container. A ScrollPane handles its own events and performs its own scrolling.
<!-- Answer ::: END -->
</div>
</div>

<div><b>4. Which Swing methods are thread-safe?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
There are only three thread-safe methods: <code><b>repaint</b></code>, <code><b>revalidate</b></code>, and <code><b>invalidate</b></code>.
<!-- Answer ::: END -->
</div>
</div>

<div><b>5. Name three Component subclasses that support painting.</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
The <code><b>Canvas</b></code>, <code><b>Frame</b></code>, <code><b>Panel</b></code>, and <code><b>Applet</b></code> classes support painting.
<!-- Answer ::: END -->
</div>
</div>

<div><b>6. What is clipping?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
Clipping is defined as the process of confining paint operations to a limited area or shape.
<!-- Answer ::: END -->
</div>
</div>

<div><b>7. What is the difference between a MenuItem and a CheckboxMenuItem?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
The <code><b>CheckboxMenuItem</b></code> class extends the <code><b>MenuItem</b></code> class and supports a menu item that may be either checked or unchecked.
<!-- Answer ::: END -->
</div>
</div>

<div><b>8. How are the elements of a BorderLayout organized?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
The elements of a <code><b>BorderLayout</b></code> are organized at the borders (North, South, East, and West) and the center of a container.
<!-- Answer ::: END -->
</div>
</div>

<div><b>9. How are the elements of a GridBagLayout organized?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
The elements of a <code><b>GridBagLayout</b></code> are organized according to a grid. The elements are of different sizes and may occupy more 
than one row or column of the grid. Thus, the rows and columns may have different sizes.
<!-- Answer ::: END -->
</div>
</div>

<div><b>10. What is the difference between a Window and a Frame?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
The <code><b>Frame</b></code> class extends the <code><b>Window</b></code> class and defines a main application window that can have a menu bar.
<!-- Answer ::: END -->
</div>
</div>

<div><b>11. What is the relationship between clipping and repainting?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
When a window is repainted by the AWT painting thread, it sets the clipping regions to the area of the window that requires repainting.
<!-- Answer ::: END -->
</div>
</div>

<div><b>12. What is the relationship between an event-listener interface and an eventadapter class?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
An event-listener interface defines the methods that must be implemented by an event handler for a particular event. An event adapter provides a default 
implementation of an event-listener interface.
<!-- Answer ::: END -->
</div>
</div>

<div><b>13. How can a GUI component handle its own events?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
A GUI component can handle its own events, by implementing the corresponding event-listener interface and adding itself as its own event listener.
<!-- Answer ::: END -->
</div>
</div>

<div><b>14. What advantage do Java’s layout managers provide over traditional windowing systems?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
Java uses layout managers to lay out components in a consistent manner, across all windowing platforms. Since layout managers aren’t tied to absolute 
sizing and positioning, they are able to accomodate platform-specific differences among windowing systems.
<!-- Answer ::: END -->
</div>
</div>


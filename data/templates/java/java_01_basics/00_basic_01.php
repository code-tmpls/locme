<div><b>2. What is the Difference between JDK and JRE ?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
The Java Runtime Environment (JRE) is basically the Java Virtual Machine (JVM) where your Java programs are being executed. 
It also includes browser plugins for applet execution. The Java Development Kit (JDK) is the full featured Software Development 
Kit for Java, including the JRE, the compilers and tools (like <code><b>JavaDoc</b></code>, and <code><b>Java Debugger</b></code>), in order for a 
user to develop, compile and execute Java applications.

<div class="list-group">
<div class="list-group-item appAnswer">
<!-- Answer ::: START -->
Active/Passive cluster configuration is a combination of two nodes, one called a primary node and another called the secondary node.<br/><br/>
The primary node suporst the clients and the secondary node acts are a failover backup node. An Active/Active cluster configuration is two simultaneous 
mirrored active/passive configurations.

<div><b>2. What are the states of Promises in ES6?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
Promises mainly possess three states as follows:
<ul>
<li><b>Pending:</b> This refers to the initial state of every promise. It indicates that the result has not yet been computed.</li>
<li><b>Fulfilled:</b> It refers to the completion of a task.</li>
<li><b>Rejected:</b> It indicates the failure that arises during computation.</li>
</ul>
The promise will be immutable once it has been fulfilled or rejected. A rejected function and a resolve function are the two arguments passed into the 
<code><b>Promise()</b></code> constructor. It returns either the first or second parameter, depending on the asynchronous operation

<div><b>1. What are Promises in ES6?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
Asynchronous programming is a concept found in JavaScript. The processes are run separately from the main thread in asynchronous programming. 
Promises are the most convenient approach to deal with asynchronous programming in ES6. Depending on the outcome of the procedure, a promise 
can be refused or resolved. Callbacks were used to cope with asynchronous programming before promises were introduced in ES6.<br/><br/>
However, it caused the problem of callback hell, which was addressed with the introduction of promises.<br/><br/>
(A callback is a function that is performed after another function has completed. When working with events in JavaScript, callback is very 
useful. As an argument to another function, we pass a function into another function.<br/><br/>
When we use callbacks in our web applications, it's common for them to get nested. Excessive callback usage clogs up your web application 
and leads to callback hell).

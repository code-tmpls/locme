<div><b>1. What is the Prototype Design Pattern?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
The Prototype Pattern creates new objects, but instead of returning uninitialized objects, it returns objects with values copied from a 
prototype - or sample - object. The Properties pattern is another name for the Prototype pattern.<br/><br/>
The initialization of business objects with values that match the database's default settings is an example of where the Prototype pattern 
comes in handy. The default values from the prototype object are replicated into a newly generated business object.<br/><br/>
The Prototype pattern is rarely used in traditional languages, but JavaScript, as a prototypal language, employs it in the creation of 
new objects and prototypes.

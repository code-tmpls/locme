<div><b>2. Explain Few Design patterns.</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
The design pattern is a reusable solution to a commonly occurring problem in software design. Some of the design patterns are:<br/><br/>
<ol>
<li><b>Behavioral Patterns:</b><br/><br/>
It focus on communication between objects.<br/><br/>
</li>
<li><b>Creational design pattern:</b><br/><br/>
It focus on ways to create objects or classes.<br/>
<code><b>Factory method</b></code>, <code><b>Abstract Factory</b></code>, <code><b>Builder</b></code>, <code><b>Prototype</b></code>, and 
<code><b>Singleton</b></code> are the categories of Creational design pattern.<br/><br/></li>
<li><b>Concurrency design patterns:</b><br/><br/>
It focus with multi-thread programming paradigms.<br/><br/></li>
<li><b>Structural design pattern:</b><br/><br/>
These patterns deal with different classes and objects to provide new functionality.<br/>
<code><b>Decorator</b></code>, <code><b>proxy</b></code> are the categories of structural design patterns.<br/><br/></li>
<li><b>Architectural design patterns: </b><br/><br/>
These patterns used to deal with architectural designs. Like: <code><b>MVC (Model-View-Controller)</b></code>, 
<code><b>MVVM (Model-View-ViewModel)</b></code> and <code><b>MVP (Model-View-presenter)</b></code>.</li>
</ol>

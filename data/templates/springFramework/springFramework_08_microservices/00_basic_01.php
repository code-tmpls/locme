<div><b>3. Explain Vertical and Horizontal Scaling in a Microservice Application?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
Microservice Architecture Application can be scaled Vertically and Horizontally :<br/><br/>
<ul>
<li><b>Scaling Vertically :</b><br/> It can be done by increasing the allocation of heap memory.<br/><br/></li>
<li><b>Scaling Horizontally :</b><br/> It can be done by making more than one instances available for a service and maintaining traffic distribution 
using Load Balancers.</li>
</ul>

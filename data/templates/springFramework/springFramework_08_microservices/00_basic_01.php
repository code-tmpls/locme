<div><b>1. What is 12 Factor App in Microservices?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
The Twelve-Factor App is a recent methodology (and/or a manifesto) for writing web applications which run as a service. They are:<br/><br/>
<ol>
<li><b>Codebase :</b><br/><br/>
One codebase, multiple deploys. This means that we should only have one codebase for different
versions of a microservices. Branches are ok, but different repositories are not.<br/><br/></li>
<li><b>Dependencies :</b><br/><br/>
Explicitly declare and isolate dependencies. The manifesto advices against relying on preinstalled softwares or libraries on the host machine. Every 
dependency should be put into <code><b>pom.xml</b></code> or <code><b>build.gradle</b></code> file.<br/><br/></li>
</ol>

<!-- Answer ::: END -->
</div>
</div>


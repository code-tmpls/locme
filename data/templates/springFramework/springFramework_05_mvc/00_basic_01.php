<div><b>2. Explain DispatcherServlet in Spring Framework.</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
The Spring Web MVC framework is designed around a <code><b>DispatcherServlet</b></code> that handles all the HTTP requests and responses.
<!-- Answer ::: END -->
</div>
</div>

<div><b>3. Explain WebApplicationContext in Spring Web MVC Framework.</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
The <code><b>WebApplicationContext</b></code> is an extension of the plain <code><b>ApplicationContext</b></code> that has some extra features necessary 
for web applications. It differs from a normal <code><b>ApplicationContext</b></code> in that it is capable of resolving themes, and that it 
knows which servlet it is associated with.
<!-- Answer ::: END -->
</div>
</div>

<div><b>4. What is Controller in Spring Web MVC framework?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
Controllers provide access to the application behavior that you typically define through a service interface. Controllers interpret 
user input and transform it into a model that is represented to the user by the view. Spring implements a controller in a very 
abstract way, which enables you to create a wide variety of controllers.
<!-- Answer ::: END -->
</div>
</div>

<div><b>5. Explain @Controller annotation in Spring Web MVC framework?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
The <code><b>@Controller</b></code> annotation indicates that a particular class serves the role of a controller. Spring does not require you to 
extend any controller base class or reference the Servlet API.
<!-- Answer ::: END -->
</div>
</div>

<div><b>6. Explain @RequestMapping annotation in Spring Web MVC framework?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
The <code><b>@RequestMapping</b></code> annotation is used to map a URL to either an entire class or a particular handler method.
<!-- Answer ::: END -->
</div>

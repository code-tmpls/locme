<div><b>4. What is RAD model?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
The Rapid Application Development model is based on prototyping and iterative model with less specific planning. In general, RAD approach to 
software development means putting lesser emphasis on planning tasks and more emphasis on development and coming up with a prototype.<br/><br/>
RAD should be used only when a system can be modularized to be delivered in an incremental manner. It should be used only if the budget permits 
use of automated code generating tools.


<div><b>1. What is Spring?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
Spring is an open source development framework for Enterprise Java. The core features of the Spring Framework can be used in developing any Java 
application, but there are extensions for building web applications on top of the Java EE platform. Spring framework targets to make Java EE development 
easier to use and promote good programming practice by enabling a POJO based programming model.
<!-- Answer ::: END -->
</div>
</div>

<div><b>2. What are benefits of Spring Framework?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
<b>Benefits of Spring Framework :</b>
<ul>
<li><b>Lightweight:</b> Spring is lightweight when it comes to size and transparency. The basic version of spring framework is around 2MB.</li>
<li><b>Inversion of control (IOC):</b> Loose coupling is achieved in Spring, with the Inversion of Control technique. The objects give
their dependencies instead of creating or looking for dependent objects.</li>
<li><b>Aspect oriented (AOP):</b> Spring supports Aspect oriented programming and separates application business logic from system services.</li>
<li>Container: Spring contains and manages the life cycle and configuration of application objects.</li>
<li><b>MVC Framework:</b> Springâ€™s web framework is a well-designed web MVC framework, which provides a great alternative to web frameworks.</li>
<li><b>Transaction Management:</b> Spring provides a consistent transaction management interface that can scale down to a local transaction and scale up to 
global transactions (JTA).</li>
<li><b>Exception Handling:</b> Spring provides a convenient API to translate technology-specific exceptions (thrown by JDBC, Hibernate,
or JDO) into consistent, unchecked exceptions.</li>
</ul>
<!-- Answer ::: END -->
</div>
</div>


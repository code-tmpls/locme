
<div><b>1. What is Spring Java-Based Configuration? Give some annotation example.</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
Java based configuration option enables you to write most of your Spring configuration without XML but with the help of few 
Java-based annotations. An example is the <code><b>@Configuration</b></code> annotation, that indicates that the class can be used by the Spring 
IoC container as a source of bean definitions. Another example is the <code><b>@Bean</b></code> annotated method that will return an object that 
should be registered as a bean in the Spring application context.
<!-- Answer ::: END -->
</div>
</div>

<div><b>2. What is Annotation-based container configuration?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
An alternative to XML setups is provided by annotation-based configuration which relies on the bytecode metadata for wiring 
up components instead of angle-bracket declarations. Instead of using XML to describe a bean wiring, the developer moves the 
configuration into the component class itself by using annotations on the relevant class, method, or field declaration.
<!-- Answer ::: END -->
</div>
</div>

<div><b>3. How do you turn on annotation wiring?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
Annotation wiring is not turned on in the Spring container by default. In order to use annotation based wiring we must enable it 
in our Spring configuration file by configuring <code><b>&lt;context:annotation-config/&gt;</b></code> element.
<!-- Answer ::: END -->
</div>
</div>

<div><b>4. Explain @Required annotation in Spring Framework.</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
This annotation simply indicates that the affected bean property must be populated at configuration time, through an explicit property value in a bean 
definition or through autowiring. The container throws <code><b>BeanInitializationException</b></code> if the affected bean property has not been populated.
<!-- Answer ::: END -->
</div>
</div>

<div><b>5. Explain @Autowired annotation in Spring Framework.</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
The <code><b>@Autowired</b></code> annotation provides more fine-grained control over where and how autowiring should be accomplished. It can be used to 
autowire bean on the setter method just like <code><b>@Required</b></code> annotation, on the constructor, on a property or on methods with arbitrary names 
and/or multiple arguments.
<!-- Answer ::: END -->
</div>
</div>

<div><b>6. Explain @Qualifier annotation in Spring Framework.</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
When there are more than one beans of the same type and only one is needed to be wired with a property, the <code><b>@Qualifier</b></code> annotation is 
used along with <code><b>@Autowired</b></code> annotation to remove the confusion by specifying which exact bean will be wired.
<!-- Answer ::: END -->
</div>
</div>


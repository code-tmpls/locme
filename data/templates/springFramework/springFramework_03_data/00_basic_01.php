<div><b>6. How can we integrate Spring and Hibernate using HibernateDaoSupport?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
Use Spring’s SessionFactory called LocalSessionFactory. The integration process is of 3 steps:<br/><br/>
<ul>
<li>Configure the Hibernate <code><b>SessionFactory</b></code>.</li>
<li>Extend a DAO Implementation from <code><b>HibernateDaoSupport</b></code>.</li>
<li>Wire in Transaction Support with AOP.</li>
</ul>
<!-- Answer ::: END -->
</div>
</div>

<div><b>7. What are the types of the transaction management Spring support?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
Spring supports two types of transaction management:<br/><br/>
<ul>
<li><b>Programmatic Transaction Management:</b><br/> This means that you have managed the transaction with the help of programming.
That gives you extreme flexibility, but it is difficult to maintain.<br/><br/></li>
<li><b>Declarative Transaction Management:</b><br/> This means you separate transaction management from the business code. You only
use annotations or XML based configuration to manage the transactions.</li>
</ul>
<!-- Answer ::: END -->
</div>
</div>

<div><b>8. What are the benefits of the Spring Framework’s transaction management?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
<ul>
<li>It provides a consistent programming model across different transaction APIs such as JTA, JDBC, Hibernate, JPA, and JDO.</li>
<li>It provides a simpler API for programmatic transaction management than a number of complex transaction APIs such as JTA.</li>
<li>It supports declarative transaction management.</li>
<li>It integrates very well with Spring’s various data access abstractions.</li>
</ul>
<!-- Answer ::: END -->
</div>
</div>

<div><b>9. Which Transaction management type is more preferable?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
Most users of the Spring Framework choose declarative transaction management because it is the option with the least impact 
on application code, and hence is most consistent with the ideals of a non-invasive lightweight container. Declarative transaction 
management is preferable over programmatic transaction management though it is less flexible than programmatic transaction 
management, which allows you to control transactions through your code.
<!-- Answer ::: END -->
</div>
</div>



<div><b>1. How can JDBC be used more efficiently in the Spring framework?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
When using the Spring JDBC framework the burden of resource management and error handling is reduced. So developers only
need to write the statements and queries to get the data to and from the database. JDBC can be used more efficiently with the
help of a template class provided by Spring framework, which is the <code><b>JdbcTemplate</b></code>.
<!-- Answer ::: END -->
</div>
</div>

<div><b>2. Explain about JdbcTemplate.</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
<code><b>JdbcTemplate</b></code> class provides many convenience methods for doing things such as converting database data into primitives or
objects, executing prepared and callable statements, and providing custom database error handling.
<!-- Answer ::: END -->
</div>
</div>

<div><b>3. Explain about Spring DAO support.</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
The Data Access Object (DAO) support in Spring is aimed at making it easy to work with data access technologies like JDBC, 
Hibernate or JDO in a consistent way. This allows us to switch between the persistence technologies fairly easily and to code 
without worrying about catching exceptions that are specific to each technology.
<!-- Answer ::: END -->
</div>
</div>

<div><b>4. What are the ways to access Hibernate by using Spring?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
There are two ways to access Hibernate with Spring:<br/><br/>
<ul>
<li>Inversion of Control with a Hibernate Template and Callback.</li>
<li>Extending <code><b>HibernateDAOSupport</b></code> and Applying an AOP Interceptor node.</li>
</ul>
<!-- Answer ::: END -->
</div>
</div>


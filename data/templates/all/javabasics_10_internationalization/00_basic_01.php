
<div class="appQuestion"><b>85. What is Locale in Java?</b></div>
<div class="list-group">
<div class="list-group-item appAnswer">
<!-- Answer ::: START -->
A Locale object represents a specific geographical, political, or cultural region. It is used to locale-sensitive operations in Java. It helps is following 
the local conventions of a country, native or region. These conventions can be for formatting the dates, money, numbers etc.
<!-- Answer ::: END -->
</div>
</div>


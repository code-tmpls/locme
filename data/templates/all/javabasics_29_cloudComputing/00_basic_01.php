<div class="appQuestion"><b>452. Explain the various modes of Software as a Service (SaaS) cloud environment?</b></div>
<div class="list-group">
<div class="list-group-item appAnswer">
<!-- Answer ::: START -->
Software as a Service (SaaS) is used to offer different kinds of software applications in a Cloud environment. Generally these are offered on
subscription basis. Different modes of SaaS are :
<ul>
<li><b>Simple multi-tenancy :</b><br/><br/> In this setup, each client gets its own resources. These resources are not shared with other clients. It is more 
secure option, since there is no sharing of resources. But it an inefficient option, since for each client more money is needed to scale it with the 
rising demands. Also it takes time to scale up the application in this mode.</li>
<li><b>Fine grain multi-tenancy :</b><br/><br/> In this mode, the feature provided to each client is same. The resources are shared among multiple clients. 
It is an efficient mode of cloud service, in which data is kept private among different clients but computing resources are shared. Also it is easier and 
quicker to scale up the SaaS implementation for different clients.</li>
</ul>

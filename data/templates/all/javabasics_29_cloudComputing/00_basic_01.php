<div class="list-group">
<div class="list-group-item appAnswer">
<!-- Answer ::: START -->
There are ten main benefits of Cloud Computing :
<ol>
<li><b>Flexibility :</b> The businesses that have fluctuating bandwidth demands need the flexibility of Cloud Computing. If you need high bandwidth, you 
can scale up your cloud capacity. When you do not need high bandwidth, you can just scale down. There is no need to be tied into an inflexible fixed capacity
infrastructure.</li>
<li><b>Disaster Recovery :</b> Cloud Computing provides robust backup and recovery solutions that are hosted in cloud. Due to this there is no need to 
spend extra resources on homegrown disaster recovery. It also saves time in setting up disaster recovery.</li>
<li><b>Automatic Software Updates :</b> Most of the Cloud providers give automatic software updates. This reduces the extra task of installing new software
version and always catching up with the latest software installs.</li>
<li><b>Low Capital Expenditure :</b> In Cloud computing the model is Pay as you Go. This means there is very less upfront capital expenditure. There is a 
variable payment that is based on the usage.</li>
<li><b>Collaboration :</b> In a cloud environment, applications can be shared between teams. This increases collaboration and communication among team 
members.</li>
<li><b>Remote Work :</b> Cloud solutions provide flexibility of working remotely. There is no on site work. One can just connect from anywhere and start 
working.</li>
<li><b>Security :</b> Cloud computing solutions are more secure than regular onsite work. Data stored in local servers and computers is prone to security 
attacks. In Cloud Computing, there are very few loose ends. Cloud providers give a secure working environment to its users.</li>
<li><b>Document Control :</b> Once the documents are stored in a common repository, it increases the visibility and transparency among companies and their 
clients. Since there is one shared copy, there are fewer chances of discrepancies.</li>
<li><b>Competitive Pricing :</b> In Cloud computing there are multiple players, so they keep competing among themselves and provide very good pricing. This 
comes out much cheaper compared to other options.</li>
<li><b>Environment Friendly :</b> Cloud computing saves precious environmental resources also. By not blocking the resources and bandwidth.</li>
</ol>

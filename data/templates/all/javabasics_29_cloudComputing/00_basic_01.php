<div class="appQuestion"><b>443. What are the different layers of Cloud computing?</b></div>
<div class="list-group">
<div class="list-group-item appAnswer">
<!-- Answer ::: START -->
Three main layers of Cloud computing are as follows:
<ul>
<li><b>Infrastructure as a Service (IAAS) :</b> IAAS providers give low-level abstractions of physical devices. Amazon Web Services (AWS) is an example
of IAAS. AWS provides EC2 for computing, S3 buckets for storage etc.<br/><br/>
Mainly the resources in this layer are hardware like memory, processor speed, network bandwidth etc.</li>
<li><b>Platform as a Service (PAAS) :</b> PAAS providers offer managed services like Rails, Django etc. One good example of PAAS is Google App Engineer. These
are the environments in which developers can develop sophisticated software with ease.<br/><br/>
Developers just focus on developing software, whereas scaling and performance is handled by PAAS provider.</li>
<li><b>Software as a Service (SAAS): SAAS provider offer an actual working software application to clients.<br/><br/> 
Salesforce and Github are two good examples of SAAS. They hide the underlying details of the software and just provide an interface to work on the system. 
Behind the scenes the version of Software can be easily changed.</li>
</ul>

<div><b>What is Babel?</b></div>
<div class="list-group">
<div class="list-group-item">
<!-- Answer ::: START -->
Babel is an open-source JavaScript transpiler that converts ECMAScript 2015+ (ES6+) code into a backwards compatible version of JavaScript 
that can be run by previous JavaScript engines. Babel is a popular tool for exploiting the JavaScript programming language's latest capabilities.<br/><br/>
Babel plugins are used to convert syntax that isn't widely supported into a version that is backwards compatible. Arrow functions, for example, 
which are defined in ES6, are translated to ordinary function declarations. It's also possible to translate non-standard JavaScript syntax, such 
as JSX. Babel may automatically inject core-js polyfills for support capabilities that aren't available in JavaScript environments. Static methods 
like Array.from and built-ins like Promise, for example, are only accessible in ES6+, but they can be utilised in previous contexts by using core-js.
